// Bytecounter demonstrates an implementation of io.Writer that counts bytes.
package bytecounter

//!+bytecounter

type ByteCounter int

func (c *ByteCounter) Write(p []byte) (int, error) {
	*c += ByteCounter(len(p)) // convert int to ByteCounter
	return len(p), nil
}
